    public FormCadastroFuncionario(String id){
        this();
        this.id = id;
        
        //exibir os dados da categoria selecionada
        Funcionario ca = new FuncionarioDAO().pesquisarPorIdFuncionario(id);
        if(ca != null){
            txtId.setText(Integer.toString(ca.getId()));
            //cbxIdEmpresa.setText(Integer.toString(ca.getId_empresa()));
            DefaultComboBoxModel m = (DefaultComboBoxModel)cbxIdEmpresa.getModel();
            
            for (int i = 0; i < m.getSize(); i++) {
                Funcionario cat = (Funcionario)m.getElementAt(i);
                if(cat.getId() == ca.getId_empresa()){/////pegar a categoria certa
                    cbxIdEmpresa.setSelectedIndex(i);
                    break;
                }
            }
            
            txtNome.setText(String.valueOf(ca.getNome()));
            txtEmail.setText(String.valueOf(ca.getEmail()));///
            //txtCpf.setText(ca.getCpf()); 
            txtTelefone.setText(String.valueOf(ca.getTelefone()));
            txtEndereco.setText(String.valueOf(ca.getEndereco()));
            txtCidade.setText(String.valueOf(ca.getCidade()));
            
        }
    }

    private void preencherComboFuncionario(){
         
        List<Funcionario> lista = new FuncionarioDAO().listarFuncionario();
        
        if(lista != null){
            DefaultComboBoxModel m = new DefaultComboBoxModel();
            for(Funcionario pro : lista){
                m.addElement(pro);
            }
            cbxIdEmpresa.setModel(m);
        }
    }


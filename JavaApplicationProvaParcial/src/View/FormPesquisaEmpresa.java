    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        //if (evt.getKeyCode() == KeyEvent.VK_ENTER){ //PARA quando apenas o enter pesquisar

            //recuperar o nome utilizado na pesquisa
            String nome = txtNome.getText();

            if(nome.isEmpty()){
                //exibir todos os registros da tabela
                preencherTabela(new EmpresaDAO().listarEmpresa());
            }else{
                //pesquisar pelo nome
                preencherTabela(
                    new EmpresaDAO().pesquisarPorNomeEmpresa(nome)
                );
            }
            //}end do keyEvent

    }//GEN-LAST:event_txtNomeKeyReleased



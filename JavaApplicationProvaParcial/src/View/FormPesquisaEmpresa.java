    private void configurarForm(){
        this.setTitle("Pesquisa de empresa");
        this.setResizable(false);
        this.setClosable(true);
        configurarTabela();
        preencherTabela(new EmpresaDAO().listarEmpresa());
    }
    
    private void configurarTabela(){
        DefaultTableModel m = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Nome");
        m.addColumn("Pais");
        tabDados.setModel(m);
    }  
    
    private void preencherTabela(List<Empresa> lista) {


        if (lista != null) {
            if (lista.size() > 0) {
                configurarTabela();
                DefaultTableModel m = (DefaultTableModel)tabDados.getModel();
                for(Empresa obj : lista){
                    m.addRow(new Object[]
                        {
                            obj.getId(),
                            obj.getNome(),
                            obj.getPais()// == 0 ? "nao" : "sim"
                        }
                    );
                }
                tabDados.setModel(m);
            } else {
                configurarTabela();//ele volta os dados quando digita de novo no textfield
                JOptionPane.showMessageDialog(null, "A tabela não contém dados.", "Controle_funcionario",
                        JOptionPane.INFORMATION_MESSAGE
                );
            }
        } else {
            JOptionPane.showMessageDialog(null,"Ocorreu um erro.","Controle_funcionario",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }
